//=================DATA TYPES=================
// Define Variables
let javascriptIsFun = true;
console.log('javascriptIsFun');

//Print to the console using typeof
console.log(typeof true);
console.log(typeof javascriptIsFun);
console.log(typeof 23);
console.log(typeof 'username');

//Changing a variable value
javascriptIsFun = 'This is a severe affirmation';

//Undefined values are both a value and a type
let year;
console.log(year);
console.log(typeof year);

//Null
let nullValue = null;
console.log(nullValue); // Returns object due to legacy reasons
