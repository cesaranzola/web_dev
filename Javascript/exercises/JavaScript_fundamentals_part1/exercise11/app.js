const daysOfWeekGerman = 'Donnerstag';

switch (daysOfWeekGerman) {
	case 'Montag':
		console.log('Montag is the first day of the week.');
		break;
	case 'Dienstag':
		console.log('Dienstag is the second day of the week.');
		break;
	case 'Mittwoch':
		console.log('Mittwoch is the third day of the week.');
		break;
	case 'Donnerstag':
		console.log('Donnerstag is the fourth day of the week.');
		break;
	case 'Freitag':
		console.log('Freitag is the fifth day of the week.');
		break;
	case 'Samstag':
		console.log('Samstag is the sixth day of the week.');
		break;
	case 'Sonntag':
		console.log('Sonntag is the seventh day of the week.');
		break;
	default:
		console.log('Not a valid day of the week');
}

