// ================= Switch statement =================

const language = 'Kaparrapo';

switch (language) {
	case 'Mandarin':
		console.log('It has the greatest number of native speakers.');
		break;
	case 'Spanish':
		console.log('2nd place in the number of native speakers.');
		break;
	case 'English':
		console.log('3rd place in the number of native speakers.');
		break;
	case 'Hindi':
		console.log('4rd place in the number of native speakers.');
		break;
	case 'Arabic':
		console.log('5th place in the number of native speakers.');
		break;
	default:
		console.log('Great language too!');
}

